          <article>
            <h4>Propósitos del Observatorio</h4>
            <br>
            <p>Realizar observaciones en el firmamento. Ofrecer adiestramiento en el uso del Telescopio y sus periféricos a Profesores, Estudiantes e Investigadores de la UNA. Formación científica y extensión universitaria para los estudiantes de todos los niveles de la educación, y aficionados de la Astronomía. Integrar grupos de observación con astrónomos paraguayos, japoneses y de otras naciones, como sistema de trabajo corporativo. Efectuar investigaciones dirigidas, en conjunción con programas internacionales, sobre:<br>• Observaciones fotométricas sobre estrellas variables y dobles.<br>• Estudio de objetos, como cometas y asteroides.<br>• Imágenes de objetos celestiales.<br><br>Crear base de datos y organizar reuniones y seminarios interdisciplinarios, con ciencias que tienen en común aspectos observacionales, estadísticos y de computación. Incentivar y apoyar investigaciones sobre observaciones del espacio celeste.</p>
          </article>
        </div>
        <div class="wrap_2_2">
          <img src="assets/img/caracteristicas.jpg" alt="caracteristicas_observatorio">
          <br><br>
          <article>
            <h4>Características del edificio</h4>
            <br>
            <p>El observatorio ha sido construido en base a estrictos requerimientos establecidos por los técnicos japoneses. Es de techo corredizo y ocupa una superficie aproximada de 300 m2. Consta de tres zonas: en la primera se encuentran dos oficinas y la sala de control. En la segunda el telescopio, en un área climatizada, con piso de madera y base antivibración. Y la tercera es la zona de recorrido del techo corredizo.<br><br>El telescopio está asentado sobre un cabezal de hormigón armado a 0,50 mts. del nivel del terreno, apoyado en una base profunda (tubulón), cuya cota de fundación está a 5,50 mts. de profundidad y aislada en su parte superior con tergopol, para absorber las vibraciones.<br><br>El techo corredizo está fabricado con chapa de zinc colocado sobre terciada fenólica, y cuenta con una membrana de aislación atérmica aluminizada. Está montado en cabriadas metálicas de perfiles. Se desliza accionado por motores eléctricos sincronizados entre sí y el telescopio.</p>
