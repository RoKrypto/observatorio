        <h3>Sobre el Observatorio</h3>
        <br><hr><br>
        <p class="copete">El Observatorio Astrónomico de la Facultad Politécnica de la UNA fue inaugurado el 6 de junio de 2000. Es una cúpula deslizable donada por el Gobierno japonés, cuenta con untelescopio reflector de 45 cm (distancia focal 5.400 mm) y con equipos de obtención de imágenes: Cámaras CCD (Charge Coupled Devise), dispositivo de acoplamiento de cargas.</p>
